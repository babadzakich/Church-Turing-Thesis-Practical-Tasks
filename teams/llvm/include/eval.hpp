// ------------------------------------------------------------
//  Задание 1. Свободные переменные
// ------------------------------------------------------------
//
//  Верни множество всех свободных переменных терма t.
//
//  Правила:
//    FV(x)     = {x}
//    FV(λx.M)  = FV(M) \ {x}
//    FV(M N)   = FV(M) ∪ FV(N)
//
std::set<std::string> free_vars(std::shared_ptr<Term> t);

// ------------------------------------------------------------
//  Задание 2. Генерация свежей переменной
// ------------------------------------------------------------
//
//  Верни имя переменной, которого нет в множестве `used`.
//  Простая стратегия: попробуй "a", "b", ..., "z",
//  потом "a0", "a1", ... — первое свободное.
//
std::string fresh_var(const std::set<std::string> &used);

// ------------------------------------------------------------
//  Задание 3. Подстановка
// ------------------------------------------------------------
//
//  substitute(M, x, N)  ≡  M[x := N]
//  Замени все свободные вхождения переменной x в терме M на N.
//
//  Важно: избегай захвата переменных!
//  Если встречаешь (λy.M') и y ∈ FV(N), переименуй y в свежую
//  переменную перед рекурсивным спуском (α-переименование).
//
std::shared_ptr<Term> substitute(std::shared_ptr<Term> m, const std::string &x,
                                 std::shared_ptr<Term> n);

// ------------------------------------------------------------
//  Задание 4. Одна β-редукция (левосторонняя, нормальный порядок)
// ------------------------------------------------------------
//
//  Найди самый левый, самый внешний редекс и выполни одну редукцию.
//  Редекс — это аппликация вида ((λx.M) N).
//
//  Верни { новый_терм, true }  если редукция была выполнена,
//         { исходный_терм, false } если редексов нет (терм в НФ).
//
std::pair<std::shared_ptr<Term>, bool> beta_step(std::shared_ptr<Term> t);

// ------------------------------------------------------------
//  Задание 5. Нормализация
// ------------------------------------------------------------
//
//  Применяй beta_step, пока можно (или пока не достигнут лимит шагов).
//  Если за `max_steps` шагов нормальная форма не достигнута —
//  брось исключение std::runtime_error("step limit exceeded").
//
//  Это защитит нас от Ω = (λx.xx)(λx.xx) и других дивергентов.
//
std::shared_ptr<Term> normalize(std::shared_ptr<Term> t, int max_steps = 1000);

