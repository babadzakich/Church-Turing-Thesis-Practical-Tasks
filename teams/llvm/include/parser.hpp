//   аппликация:    M N   (левоассоциативна)
//   скобки:        (M)
//
// Примеры допустимых строк:
//   "x"
//   "\\x.x"
//   "(\\x.x) y"
//   "\\f.\\x. f (f x)"
//
// Бросает std::runtime_error при синтаксической ошибке.
std::shared_ptr<Term> parse(const std::string& input);
